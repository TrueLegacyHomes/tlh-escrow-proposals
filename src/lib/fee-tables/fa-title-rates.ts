/**
 * Look up the First American EAGLE Owner's Policy title fee for a given
 * purchase price.
 *
 * Finds the largest threshold that is less than or equal to the purchase price
 * and returns the corresponding fee. If the purchase price is below the
 * minimum threshold, the first entry's fee is returned.
 */
export function lookupTitleFee(purchasePrice: number): number {
  let fee = FA_TITLE_RATES[0][1];

  for (const [threshold, rate] of FA_TITLE_RATES) {
    if (threshold <= purchasePrice) {
      fee = rate;
    } else {
      break;
    }
  }

  return fee;
}
